return {
	"hrsh7th/nvim-cmp",
	dependencies = {
		"hrsh7th/cmp-nvim-lsp", -- –ò–Ω—Ç–µ–≥—Ä–∞—Ü–∏—è —Å LSP
		"hrsh7th/cmp-nvim-lua", -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –¥–ª—è Lua API NeoVim
		"hrsh7th/cmp-buffer", -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –∏–∑ –±—É—Ñ–µ—Ä–∞
		"hrsh7th/cmp-path", -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –ø—É—Ç–µ–π
		"hrsh7th/cmp-cmdline", -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
		"saadparwaiz1/cmp_luasnip", -- –ò–Ω—Ç–µ–≥—Ä–∞—Ü–∏—è —Å LuaSnip
		"L3MON4D3/LuaSnip", -- –°–Ω–∏–ø–ø–µ—Ç—ã
		"zbirenbaum/copilot-cmp", -- GitHub Copilot
	},
	event = "InsertEnter",
	config = function()
		local cmp = require("cmp")
		local luasnip = require("luasnip")

		require("luasnip.loaders.from_vscode").lazy_load()

		local select_opts = { behavior = cmp.SelectBehavior.Select }

		cmp.setup({
			snippet = {
				expand = function(args)
					luasnip.lsp_expand(args.body)
				end,
			},
			sources = cmp.config.sources({
				{ name = "nvim_lsp", group_index = 2, keyword_length = 1 }, -- –ò—Å—Ç–æ—á–Ω–∏–∫ –¥–ª—è LSP
				{ name = "luasnip",  group_index = 2, keyword_length = 2 }, -- –ò—Å—Ç–æ—á–Ω–∏–∫ –¥–ª—è —Å–Ω–∏–ø–ø–µ—Ç–æ–≤
				{ name = "buffer",   group_index = 2, keyword_length = 3 }, -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –∏–∑ –±—É—Ñ–µ—Ä–∞
				{ name = "copilot",  group_index = 2, keyword_length = 1 }, -- –ò—Å—Ç–æ—á–Ω–∏–∫ –¥–ª—è Copilot
				{ name = "path" },              -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –ø—É—Ç–µ–π
				{ name = "cmdline" },           -- –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
			}),
			window = {
				documentation = cmp.config.window.bordered()
			},
			formatting = {
				fields = { "menu", "abbr", "kind" },
				format = function(entry, item)
					local icons = {
						nvim_lsp = "Œª",
						luasnip = "‚ãó",
						buffer = "Œ©",
						path = "üñ´",
					}
					item.menu = icons[entry.source.name]
					return item
				end,
			},
			mapping = {
				["<Up>"] = cmp.mapping.select_prev_item(select_opts),
				["<Down>"] = cmp.mapping.select_next_item(select_opts),

				["<C-p>"] = cmp.mapping.select_prev_item(select_opts),
				["<C-n>"] = cmp.mapping.select_next_item(select_opts),

				["<C-u>"] = cmp.mapping.scroll_docs(-4),
				["<C-d>"] = cmp.mapping.scroll_docs(4),

				["<C-e>"] = cmp.mapping.abort(),
				["<C-y>"] = cmp.mapping.confirm({ select = true }),
				["<CR>"] = cmp.mapping.confirm({ select = false }),

				["<C-f>"] = cmp.mapping(function(fallback)
					if luasnip.jumpable(1) then
						luasnip.jump(1)
					else
						fallback()
					end
				end, { "i", "s" }),

				["<C-b>"] = cmp.mapping(function(fallback)
					if luasnip.jumpable(-1) then
						luasnip.jump(-1)
					else
						fallback()
					end
				end, { "i", "s" }),

				["<Tab>"] = cmp.mapping(function(fallback)
					local col = vim.fn.col(".") - 1
					if cmp.visible() then
						cmp.select_next_item(select_opts)
					elseif col == 0 or vim.fn.getline("."):sub(col, col):match("%s") then
						fallback()
					else
						cmp.complete()
					end
				end, { "i", "s" }),

				["<S-Tab>"] = cmp.mapping(function(fallback)
					if cmp.visible() then
						cmp.select_prev_item(select_opts)
					else
						fallback()
					end
				end, { "i", "s" }),
			},
		})
	end,
}
